func testBooleanObject(t *testing.T, obj object.Object, expected bool) bool {
	boolObj, ok := obj.(*object.Boolean)
	if !ok {
		t.Errorf("obj is not of type object.Boolean. got=%T (%+v)", obj, obj)
		return false
	}

	if boolObj.Value != expected {
		t.Errorf("boolObj.Value is incorrect. want=%t, got=%t", expected, boolObj.Value)
		return false
	}

	return true
}

